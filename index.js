const divide = function() {
    return 2000 / 100;
  };
  
  // Define the arrow function called square
  const square = (num) => {
    return num * num;
  };
  
  // Define the arrow function called add
  const add = (a, b) => {
    return a + b;
  };
  
  // Export the functions
  module.exports = {
    divide,
    square,
    add
  };
  